"""
Состояния FSM для процесса подачи заявки на недвижимость.
Эти состояния отслеживают этапы заполнения заявки пользователем.
"""

from aiogram.fsm.state import State, StatesGroup

class RequestStates(StatesGroup):
    """
    Группа состояний для процесса подачи заявки.
    Каждое состояние соответствует определенному этапу заполнения формы заявки.
    """
    # Основные данные пользователя
    name = State()              # Ввод имени пользователя
    phone = State()             # Ввод номера телефона
    
    # Параметры недвижимости
    property_type = State()     # Выбор типа недвижимости
    deal_type = State()         # Выбор типа сделки
    district = State()          # Выбор района
    budget = State()            # Выбор бюджета
    rooms = State()             # Выбор количества комнат
    
    # Этапы работы с объектами
    viewing_listings = State()  # Просмотр найденных объектов
    select_property = State()   # Выбор объектов для заявки
    comments = State()          # Ввод комментариев к заявке
    
    # Завершение
    completed = State()         # Заявка завершена